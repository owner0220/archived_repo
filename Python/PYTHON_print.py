### 기본_"안녕~!"을 찍어보자

# 쓰는데로 찍어봐
print("안녕~!")

# 변수를 이용해보자
a="안녕"
print(a)

# 리스트를 이용해보자
b=["안녕","hi","오하이요"]
print(b[0])

# 딕셔너리를 이용해 안녕 표현의 언어를 프린트 해보자
c={'안녕':'한국', 'hi':'미국','오하이요':'일본'}
print(c['안녕'])


